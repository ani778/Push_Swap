#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long num;

	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		num = (long)n * (-1);
	}
	else
		num = (long)n;
	if (num >= 10)
	{
		ft_putnbr_fd(num / 10, fd);
		ft_putnbr_fd(num % 10, fd);
	}
	else
		ft_putchar_fd(num + '0', fd);
}
