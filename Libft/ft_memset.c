#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	unsigned char	*j;
	size_t			i;

	i = 0;
	j = s;
	while (i < n)
	{
		j[i] = c;
		i++;
	}
	return (j);
}
